# 字符串
str1 = 'aa'
str2 = 'bb'

# 列表
list1 = [1, 2]
list2 = [10, 20]

# 元组
t1 = (1, 2)
t2 = (10, 20)

# 字典
dict1 = {'name': 'Python'}
dict2 = {'age': 30}

# +: 合并
print(str1 + str2)    # aabb
print(list1 + list2)  # [1, 2, 10, 20]
print(t1 + t2)        # (1, 2, 10, 20)

